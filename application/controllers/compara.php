   public function tabla_control()
    {
        $archivo='SEDENA';
        $this->load->model('compara_model');
        $data['tabla'] = $this->compara_model->control($archivo);
        $data['titulo']= 'RECIBA DE MERCANCIA';
        $data['titulo1'] = "";
        $data['contenido'] = "procesos";
        $data['selector'] = "procesos";
        $data['sidebar'] = "sidebar_campana";

        $this->load->view('header');
        $this->load->view('main', $data);
        $this->load->view('extrafooter');

}       
	   function actualiza_marca()
    {
        $data = array('marca' => $this->input->post('valor'));
        $this->db->where('id', $this->input->post('id'));
        $this->db->update('compara.detalle', $data);
    } 
  
