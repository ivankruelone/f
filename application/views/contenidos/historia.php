            <p><br /> A pesar de que a finales de 1874 un incendio acab&oacute; con la botica de Don Felipe, su esp&iacute;ritu emprendedor lo lleva en pocos meses a instalar una nueva, bajo la raz&oacute;n social de Felipe Gonz&aacute;lez y Hermanos. En el acta del ayuntamiento de Tampico, fechada el 2 de enero de 1875 dice &quot;<strong>Se concede al C. Felipe Gonz&aacute;lez Garza, autorizaci&oacute;n para la apertura de una Botica de segunda clase dado el escaso capital con el que cuenta</strong>&quot;.
            <br />
            <br />
            Al paso de los a&ntilde;os, ya con m&aacute;s capital, fruto del arduo trabajo, constituye la raz&oacute;n social <strong>Felipe G. e Hijos Sucesores</strong>, con el nombre comercial de <strong>Droguer&iacute;a y Botica Nueva</strong>, utilizando como logotipo la mitol&oacute;gica <strong>Ave F&eacute;nix</strong> que resurge de sus propias cenizas.
            <br />
            <br />
            Esta empresa se convierte en un distribuidor muy importante de&nbsp;laboratorios establecidos en M&eacute;xico y fuerte importadora de medicamentos y otros productos fabricados por las m&aacute;s acreditadas firmas Europeas y de los Estados Unidos.
