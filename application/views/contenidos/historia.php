            <p><strong>DROGUER&Iacute;A Y FARMACIA EL F&Eacute;NIX, S.A. de C.V.</strong>es una empresa fundada en la ciudad de Tampico, Tamaulipas en el a&ntilde;o de 1871, por lo que es en su ramo la m&aacute;s antigua y experimentada de la Rep&uacute;blica Mexicana.
            <br />
            <br />
            Muchas empresas por su solidez y prestigio, nos causan la impresi&oacute;n de haber existido siempre. <strong>DROGUER&Iacute;A Y FARMACIA EL F&Eacute;NIX, S.A. de C.V.</strong> inici&oacute; siendo un modesto negocio que creci&oacute; y se ha venido consolidando al paso del tiempo con perseverancia, audacia y tenacidad, pero sobre todo, por la confianza de los accionistas en el&nbsp;desarrollo del pa&iacute;s y por la preferencia de nuestros clientes.
            <br />
            <br />
            Su fundador <strong>Don Felipe Gonz&aacute;lez Garza </strong>inicia su vida profesional en el estado de Nuevo Le&oacute;n cuando recibe&nbsp;por parte del Consejo de Salubridad de dicha entidad, su t&iacute;tulo de Farmac&eacute;utico.
            <br />
            <br />
            A fines de 1870 en busca de nuevos horizontes y con visi&oacute;n comercial, se traslada a Tampico en donde al a&ntilde;o siguiente establece una Botica, iniciando con ella lo que hoy en d&iacute;a es una cadena de Superfarmacias&nbsp;que se ha mantenido como tradici&oacute;n de trabajo a trav&eacute;s de cinco generaciones.
            <br /><br /></p>
            
            
